export const seedEmployees = async () => {
  console.log("Starting to seed employees...");
  let count = 0;
  for (const emp of employeesToSeed) {
    try {
      console.log(`Syncing user: ${emp.name} (${emp.employeeId})...`);
      const result = await api.syncUser(emp as any);
      console.log(`Successfully seeded: ${emp.name}`, result);
      count++;
    } catch (e) {
      console.error(`Failed to seed: ${emp.name}`, e);
      throw e; // Rethrow to stop and show error in UI
    }
  }
  console.log(`Seeding finished. Total: ${count}`);
};
